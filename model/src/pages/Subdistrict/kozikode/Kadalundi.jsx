<p> <h3>Political history</h3>
Kadalundi, on the southern bank of Chaliyar river and the northern bank of Kadalundi River, was a part the kingdom of Parappanad during medieval period.[20] The rulers of Parappanad were vassals to the Zamorin of Calicut.[21] The headquarters of Parappanad Royal family was the coastal town of Parappanangadi in present-day Malappuram district.[22] In the 15th century CE, Parappanad Swaroopam was divided into two - Northern Parappanad (Beypore Swaroopam) and Southern Parappanad (Parappur Swaroopam).[21] Kadalundi, Vallikkunnu, and Parappanangadi, were included in Southern Parappanad.[20] Beypore, Cheruvannur, and Panniyankara, on northern bank of Chaliyar, became Northern Parappanad.[20]

It is also known the ruler of the Kingdom of Tanur (Vettathunadu Swaroopam), had assisted the Portuguese to build a fort at the island of Chaliyam, which was a part of Southern Parappanad, and was destructed during the Battle at Chaliyam fort occurred in 1571.[21] Feroke became a part of the Kingdom of Mysore in the late 18th century CE.[21] Following the Third Anglo-Mysore War and the subsequent Treaty of Seringapatam, Kadalundi became a part of Malabar District under British Raj.[21] Kadalundi was included in Eranad Taluk in the Malappuram Revenue Division of Malabar District with its Taluk headquarters at Manjeri.[23][20][24] Following the formation of the state of Kerala in 1956, Kadalundi became a part of Tirurangadi Revenue block of Tirur Taluk.[22][25] On 16 June 1969, Eranad Taluk, Tirur Taluk, Tirurangadi, and Parappanangadi, were transferred to newly formed Malappuram district.[25] However, three Revenue Villages of Tirur Taluk, namely, Feroke, Ramanattukara, and Kadalundi, remained in Kozhikode district, as they were much closer to Kozhikode city centre.[25] However Kadalundi Nagaram beach (where Kadalundi River flows into Arabian Sea, a part of Vallikkunnu Grama Panchayat), Tenhipalam, the centre of University of Calicut, and Karippur, the site of Calicut International Airport, became parts of Malappuram.[25] Now Feroke, Ramanattukara, and Kadalundi are parts of Kozhikode Taluk and Kozhikode metropolitan area</p>






