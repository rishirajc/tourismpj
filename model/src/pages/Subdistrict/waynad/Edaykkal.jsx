    <p>The Edakkal caves are two natural caves at a remote location at Edakkal, 25 km (15.5 mi) from Kalpetta in the Wayanad district of Kerala in India. They lie 1,200 m (3,900 ft) above sea level on Ambukutty Mala, near an ancient trade route connecting the high mountains of Mysore to the ports of the Malabar Coast. Inside the caves are paintings believed to date to at least 6,000 BCE,[1][2] from the Neolithic man, indicating the presence of a prehistoric settlement in this region.[3] The Stone Age carvings of Edakkal are rare and are the only known examples from South India besides those of Shenthurini, Kollam, also in Kerala.[4] The cave paintings of Shenthurini (Shendurney) forests in Kerala are of the Mesolithic era (middle stone-age).</p>
